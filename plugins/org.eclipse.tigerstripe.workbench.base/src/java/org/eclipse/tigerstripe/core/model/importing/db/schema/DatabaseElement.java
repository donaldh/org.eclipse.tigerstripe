package org.eclipse.tigerstripe.core.model.importing.db.schema;

/**
 * Abstract root class for any Database element
 * 
 * @author Eric Dillon
 * 
 */
public abstract class DatabaseElement {

	private String name;

	public DatabaseElement(String name) {
		setName(name);
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
