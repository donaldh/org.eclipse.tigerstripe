package org.eclipse.tigerstripe.annotation.java.ui;

import org.eclipse.jface.resource.ImageDescriptor;
import org.eclipse.swt.graphics.Image;
import org.eclipse.ui.plugin.AbstractUIPlugin;
import org.osgi.framework.BundleContext;

/**
 * The activator class controls the plug-in life cycle
 */
public class JavaAnnotationUIPlugin extends AbstractUIPlugin {

	// The plug-in ID
	public static final String PLUGIN_ID = "org.eclipse.tigerstripe.annotation.java.ui";

	// The shared instance
	private static JavaAnnotationUIPlugin plugin;
	
	/**
	 * The constructor
	 */
	public JavaAnnotationUIPlugin() {
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#start(org.osgi.framework.BundleContext)
	 */
	public void start(BundleContext context) throws Exception {
		super.start(context);
		plugin = this;
	}

	/*
	 * (non-Javadoc)
	 * @see org.eclipse.ui.plugin.AbstractUIPlugin#stop(org.osgi.framework.BundleContext)
	 */
	public void stop(BundleContext context) throws Exception {
		plugin = null;
		super.stop(context);
	}

	/**
	 * Returns the shared instance
	 *
	 * @return the shared instance
	 */
	public static JavaAnnotationUIPlugin getDefault() {
		return plugin;
	}
	
	public static Image createImage(String path) {
		ImageDescriptor descriptor = createImageDescriptor(path);
		return descriptor == null ? null : descriptor.createImage();
	}
	
	public static ImageDescriptor createImageDescriptor(String path) {
		 return imageDescriptorFromPlugin(PLUGIN_ID, path);
	}

}
